"""
Provides some arithmetic functions
"""
# przykladowe funkcje utils .py
def add(a: int, b: int) -> int:
    """Return the sum of two input variables"""
    return a + b


def subtract(a: int, b: int) -> int:
    """Return the substraction of two input variables"""
    return a - b


def multiply(a: int, b: int) -> int:
    """Return the multiplication of two input variables"""
    return a * b


def divide(a: int, b: int) -> float:
    """Return the division of two input variables"""
    return a / b
